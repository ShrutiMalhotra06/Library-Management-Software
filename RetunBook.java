    private void cmbIssuedIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbIssuedIDActionPerformed
        // TODO add your handling code here:
        issuedid = cmbIssuedID.getSelectedItem();
        try {
            con = connection.connect.makeConnection();
            Statement st = con.createStatement();
            String query = "select book_code,book_name,student_id,student_name,DATE_FORMAT(issue_date, '%D %M %Y'),DATE_FORMAT(curdate(), '%D %M %Y') from book_issue where issue_id='" + issuedid.toString() + "'";
            rst = st.executeQuery(query);
            if (rst.next()) {
                txtbook_code.setText(rst.getString(1));
                txtbookname.setText(rst.getString(2));
                txtstuid.setText(rst.getString(3));
                txtstuname.setText(rst.getString(4));
                txtissuedate.setText(rst.getString(5));
                txtreturndate.setText(rst.getString(6));
            } else {
                JOptionPane.showMessageDialog(null, "Data Not Found!!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_cmbIssuedIDActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        issuedid = cmbIssuedID.getSelectedItem();
        if (issuedid.equals("")) {
            JOptionPane.showMessageDialog(null, "Please Select Book Issue ID!!");
            return;
        }
        book_code = txtbook_code.getText();

        if (!issuedid.equals("")) {
            try {
                con = connection.connect.makeConnection();
                pst = con.prepareStatement("update book_issue set status='false' where issue_id='" + issuedid.toString() + "'");
                int i = pst.executeUpdate();
                if (i > 0) {
                    // Increase Update Quantiy
                    updateBookQuantity(book_code);
                    JOptionPane.showMessageDialog(null, "Return Successfully");
                } else {
                    JOptionPane.showMessageDialog(null, "Return Canceled");
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

