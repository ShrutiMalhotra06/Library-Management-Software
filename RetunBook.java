//Increase Book quantity
    public void updateBookQuantity(String boookid) {

        int res, qty = 0;
        try {
            Connection con11 = connection.connect.makeConnection();
            Statement st11 = con11.createStatement();
            String query = "select quantity from book_master where book_code='" + boookid + "'";
            ResultSet rst11 = st11.executeQuery(query);
            if (rst11.next()) {
                res = rst11.getInt(1);
                qty = res + 1;
                try {
                    Connection con22 = connection.connect.makeConnection();
                    PreparedStatement pstt = con22.prepareStatement("update book_master set quantity='" + qty + "' where book_code='" + boookid + "'");
                    pstt.executeUpdate();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            st11.close();
            con11.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }
